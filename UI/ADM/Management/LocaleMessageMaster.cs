        #region :: btnCreate_Click :: C# Enum Class를 생성합니다.

        /// <summary>
        /// C# Enum Class를 생성합니다.
        /// </summary>
        /// <param name="sender"></param>
        /// <param name="e"></param>
        private void btnCreate_Click(object sender, EventArgs e)
        {
            DataRow[] drs = viewList.GetDataTableByDataSource().Select("ISEXPORT = 1");
            StringBuilder sb = new StringBuilder();

            sb.AppendLine("/// <summary>");
            sb.AppendLine("/// 없음");
            sb.AppendLine("/// </summary>");
            sb.AppendLine("None = 0,");

            foreach (DataRow dr in drs)
            {
                sb.AppendLine("/// <summary>");
                sb.AppendLine(string.Format("/// {0}", dr["KOKR"]));
                sb.AppendLine("/// </summary>");
                sb.AppendLine(string.Format("{0} = {1},", dr["ENUMCLASS"], dr["MESSAGEID"]));
            }
            txtEnumClass.EditValue = sb;
        }

